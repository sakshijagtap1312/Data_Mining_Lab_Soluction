Write a R program to add, multiply and divide two vectors of integer type. (vector
length should be minimum 4)
=>
# Define two integer vectors of length 4
vector1 <- c(10, 20, 30, 40)
vector2 <- c(5, 4, 3, 2)

# Perform addition
addition_result <- vector1 + vector2

# Perform multiplication
multiplication_result <- vector1 * vector2

# Perform division
division_result <- vector1 / vector2

# Print the results
cat("Addition of two vectors:", addition_result, "\n")
cat("Multiplication of two vectors:", multiplication_result, "\n")
cat("Division of two vectors:", division_result, "\n")
